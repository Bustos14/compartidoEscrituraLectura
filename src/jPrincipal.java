    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jfc = new JFileChooser(".");
        jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int select = jfc.showOpenDialog(jfc);
        if (select == JFileChooser.APPROVE_OPTION) {
            dirpath = jfc.getSelectedFile().getAbsolutePath();
            File f = new File(dirpath + "\\DATOS.DAT");
            try {
                out = new FileOutputStream(f);
                ob_out = new ObjectOutputStream(out);
                for (int i = 0; i < listAlu.size(); i++) {
                    ob_out.writeObject(listAlu.get(i));
                }
                ob_out.close();
                out.close();

            } catch (FileNotFoundException ex) {
                Logger.getLogger(jPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(jPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        if (select == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "Ha cerrado sin seleccionar");
        } 
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        alu = new Alumno();
        try {
            int matricula = Integer.parseInt(txtMatricula.getText());
            if (listAlu.size() > 0) {
                for (int i = 0; i < listAlu.size(); i++) {
                    System.out.println(listAlu.get(i).getMatricula());
                    if (matricula != listAlu.get(i).getMatricula()) {
                        alu.setMatricula(matricula);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ya existe esa matricula");
                        alu.setMatricula(0);
                        break;
                    }
                }
            } else {
                alu.setMatricula(matricula);
            }

        } catch (NumberFormatException nex) {
            System.out.println("Numero mal");
        }

        Double notaMedia = Double.parseDouble(txtNotaMedia.getText());
        if (notaMedia <= 10.00 && notaMedia >= 0.00) {
            alu.setNotaMedia(notaMedia);
        } else {
            JOptionPane.showMessageDialog(this, "La nota debe estar comprendida entre 0 y 10");
        }
        alu.setNombre(txtNombre.getText());
        Date fecha = ParseFecha(txtFecha.getText());
        if(fecha!= null){
             alu.setFechaInicio(fecha);
        }
        if (alu.getMatricula() != 0 && fecha!=null && txtNombre.getText().length()>0 ) {
            listAlu.add(alu);
        }
        txtMatricula.setText("");
        txtNombre.setText("");
        txtFecha.setText("");
        txtNotaMedia.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

