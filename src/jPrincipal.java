    public Date ParseFecha(String fecha) {
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        Date fechaDate = null;
        try {
            java.util.Date nfecha = formato.parse(fecha);
            fechaDate = new java.sql.Date(nfecha.getTime());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto.\n Pruebe con el siguiente formato: dia/Mes/año");
        }
        return fechaDate;
    }

